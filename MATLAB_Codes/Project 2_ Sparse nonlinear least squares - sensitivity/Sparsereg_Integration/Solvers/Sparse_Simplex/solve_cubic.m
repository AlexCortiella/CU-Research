function out=solve_cubic(a)

A0=a(:,1)./a(:,4);
A1=a(:,2)./a(:,4);
A2=a(:,3)./a(:,4);

Q=1/9*(3*A1-A2.^2);
R=1/54*(9*A1.*A2-27*A0-2*A2.^3);
D=Q.^3+R.^2;
S=(R+sqrt(D)).^(1/3);
T=-Q./S;

z1=-1/3*A2+(S+T); 
z2=-1/3*A2-1/2*(S+T)+1/2*sqrt(-3)*(S-T);
z3=-1/3*A2-1/2*(S+T)-1/2*sqrt(-3)*(S-T);

out=[z1,z2,z3];